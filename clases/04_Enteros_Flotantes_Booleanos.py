# ejemplo de número con anotación cientifica

num_1 = 30e7
num_2 = 30e-7

print(num_1, 'tipo =>', type(num_1))
print(num_2, 'tipo =>', type(num_2))

# ejemplo de int

x = 10 

print(x, 'type =>', type(x))

# ejemplo de float

y = 5.678

print(y, 'type =>', type(y))

