# Operaciones matemáticas

num_1 = 30 # 30
num_2 = 4 # 4

print('Suma:', num_1 + num_2) # 34
print('Resta:', num_1 - num_2) # 26
print('Multiplicación:', num_1 * num_2) # 120
print('Potenciación:', num_1 ** num_2) # 810000
print('División:', num_1 / num_2) # 7.5
print('Divisón Entera:', num_1 // num_2) # &
print('Módulo de la División:', num_1 % num_2) # 2

# Operaciones con Shortcuts

num_3 = 8 # 8
print(num_3) # 8 

num_3 +=4 # 12
print(num_3) # 12

num_3 /= 2 # 6
print(num_3) # 6

num_3 **= 2 # 36
print(num_3) # 36

# Uso del PENDAS

operation_1 = num_1 + num_2 * 4 # 46
operation_2 = (num_1 + num_2)* 4  # 136
operation_3 = (num_1 + num_2) * (80 ** 3) / (7 - num_2) # 5802666.66666667

print(operation_1) # 46
print(operation_2) # 136
print(operation_3) # 5802666.66666667

# Comparación Matemática

num_1 = 30 # 30
num_2 = 4  # 4

print(num_1 > num_2) # True
print(num_1 < num_2) # False
print(num_1 >= num_2) # True
print(num_1 <= num_2) # False
print(num_1 == num_2) # False
print(num_1 != num_2) # True